#include <string.h>

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	int ct;

	ct = 0;
	if (n == 0)
	{
		return (0);
	}
	if (s1 == NULL || s2 == NULL)
		return (0);
	while (s1[ct] == s2[ct] && s1[ct] != '\0' && s2[ct] != '\0' && n > 0)
	{
		ct++;
		n--;
	}
	return (s1[ct] - s2[ct]);
}
